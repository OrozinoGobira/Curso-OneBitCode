// Função chamada quando o cursor do mouse entra no elemento associado
function mouseEnterEvent() {
  console.log('Entrada do mouse');
}

// Função chamada quando o valor de um elemento de formulário é alterado
function changeEvent() {
  console.log('Mudança no valor');
}

// Função chamada quando um elemento recebe foco
function focusEvent() {
  console.log('foco no elemento');
}

// Função chamada quando um elemento perde o foco
function blurEvent() {
  console.log('desfocado o elemento');
}

// Função chamada quando o elemento associado é clicado, exibindo um alerta
function clickEvent() {
  alert('clicou');
}
